    <p>
      The Bi-State Chapter was organized in 1985 from a need to provide a
      technical forum and a pleasant social environment to exchange ideas for
      ASHRAE members in the counties surrounding White Plains, N.Y. The New York
      City, Northern New Jersey and Connecticut Chapters were all considered too
      far to travel to meetings in New York City, Central New Jersey and
      Hartford.
    </p>

    <p>
      The initial chartering meeting was held in the offices of Wolff &amp;
      Munier, Inc. on February 2, 1985, during which Charles J. Procell was
      elected President of the steering committee for the chartering purposes.
      He had successfully chartered a chapter in Rockford, Illinois, in 1972.
    </p>

    <p>
      A membership meeting was held on March 5,1985 in the White Plains Hotel,
      where 40 ASHRAE members signed the charter petition. At this meeting, the
      name Bi-State was suggested as an invitation to our Connecticut members.
      The late Thomas S. Brown was present at the meeting. He was the Director
      and Regional Chairman of Region I. Tom was instrumental in the chartering
      process after conversations during 1984 with Charlie Procell. In a March
      11, 1985 letter to Charlie, Tom stated, "Without doubt, this Chapter will
      become one of the outstanding Chapters in Region I. I wish you and all the
      people who have worked to make this a reality, my congratulations and
      thanks."
    </p>

    <p>
      Following the March meeting, a letter was sent to the New York City
      Chapter requesting that they relinquish the counties of Putnam &amp;
      Westchester in New York and Fairfield County in Connecticut to the
      Bi-State Chapter. Similar letters were sent to the Connecticut and
      Northern New Jersey Chapters for the relinquishment of Orange and Rockland
      Counties, and Dutchess County, respectively. All three Chapter Presidents
      responded quickly in the affirmative.
    </p>

    <p>
      The April meeting included a field trip to the General Foods Headquarters,
      and the speaker at the May meeting was Mayor of the City of White Plains
      and Engineer, Alfred Del Vecchio, P.E. Thus, a tradition of high quality
      program for the monthly meetings was initiated. The formal "Petition to
      Society" was submitted following the April meeting. The charter was
      received in record time and was issued on June 4, 1985 at a meeting at the
      White Plains Hotel were ASHRAE President Bob MacDonald presented the
      charter to Chapter President Charlie Procell. The list of Presidents and
      President-Elects are enumerated below:
    </p>

