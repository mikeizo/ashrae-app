    <p>
      Initially, the chapter was holding technical seminars on energy
      conservation and heating/cooling load calculations, etc. After one year of
      operation Perry Cuomo received the CRS award in membership in 1986 and the
      following year Hank Munier received the award for the most improved
      chapter in 1987. Awards were also given to Bob Gironda for the technical
      affairs and the Region Award of Merit was given to Charles Procell. For
      1988-1989 terms the chapter received the first PAOE award. Two of our
      charter members, Paul Bourquin (June 10,1990) and Charles J. Procell
      (January25, 1992) have been elected Fellows of ASHRAE. Art Breuer, P.E.
      and Robert Roston, P.E. received awards for membership and research
      promotion, respectively, on June 10, 1990.
    </p>

    <p>
      Initial meetings were held at the White Plains Hotel and then changed to
      New York Polytechnic Institute, and then Tino's on Saw Mill River Road,
      the Marriott Hotel on Route 119, the Cabin Restaurant in Valhalla and now
      is held at the Casa Rina Restaurant in Thornwood, N.Y. The food is better
      at the same cost and the separate room provides a better environment for
      the tech sessions and the main program.
    </p>

    <p>
      We would be remiss in not mentioning William Timm and James Zonino that
      lifted the chapter to new heights at the time of their service as
      President. James Zonino's dedication to Bi-State ASHRAE will be remembered
      by all of us for a long time. He attended a meeting at the Tarrytown
      Hilton to confirm arrangements for the year's CRC 2001 one week before he
      passed away with cancer. During this time of renewal, Joseph Ting, former
      Director and Regional Chair - Region I, joined the chapter and spearheaded
      the CRC 2001 as General Chair with the "TOPS", Technical, Operations,
      Planning and Social subcommittees. The CRC was a great success that
      featured the "first paperless CRC" in Region I and a trade show, technical
      seminars, and technical tours of the Sheet Metal Workers Training Facility
      in Brewster, NY, sponsored by SMACNA. As a result of this activity the
      chapter raised $20,000 for a scholarship fund.
    </p>

    <p>
      We have all made a conscious decision to make a career in the HVAC
      Industry over the next 20-30 years. Why not take an active role in being
      part of the pleasant, friendly and educational environment at a monthly
      Bi-State ASHRAE meeting in centrally located Thornwood. Who knows you
      might find a pearl of wisdom or meet a new friend.
    </p>
